void ManagedScriptBaker::BakeFiles(const FileCollection& files, string_view target_path) const
{
    FO_STACK_TRACE_ENTRY();

    if (!target_path.empty()) {
        const string normalized_target_path = strex(target_path).normalize_path_slashes().str();

        if (!normalized_target_path.starts_with("Assemblies/") || strex(normalized_target_path).get_file_extension() != "dll") {
            return;
        }
    }

    const optional<std::filesystem::path> managed_source_dir = FindManagedSourceDir();

    if (!managed_source_dir.has_value()) {
        throw ManagedScriptBakerException("Managed C# source directory not found", "Engine/Source/Scripting/Managed/CoreScripts");
    }

    const std::filesystem::path managed_project_dir = GetManagedProjectDir();
    const std::filesystem::path managed_assemblies_output_dir = GetManagedAssembliesOutputDir(*_context);
    const vector<std::filesystem::path> generated_core_source_files = CopyManagedCoreScriptFiles(*managed_source_dir, managed_project_dir);

    vector<string> assemblies = SplitEnvList(GetEnvValue("FO_MANAGED_ASSEMBLIES"));

    if (assemblies.empty()) {
        assemblies.emplace_back("FOnline");
    }

    const string managed_pack_name = _context->PackName;
    const string project_name = GetManagedProjectBaseName();
    const array<string_view, 3> targets {"Server", "Client", "Mapper"};
    const array<string_view, 5> generated_file_suffixes {"Enums", "Types", "Entities", "Events", "Settings"};
    const bool dry_run = GetEnvValue("FO_MANAGED_SCRIPT_BAKER_DRY_RUN") != nullptr;

    struct TargetBakeTask
    {
        string Target {};
        string ResourcePath {};
        vector<std::filesystem::path> SourceFiles {};
        vector<string> References {};
        bool ShouldBake {};
    };

    vector<TargetBakeTask> target_tasks;
    target_tasks.reserve(targets.size());
    map<string, vector<std::filesystem::path>> project_sources;
    map<string, vector<string>> project_references;
    unordered_set<string> expected_generated_files;

    for (const string_view target : targets) {
        for (const string_view suffix : generated_file_suffixes) {
            expected_generated_files.emplace(MakeGeneratedManagedApiFileName(target, suffix));
        }
    }
    for (const std::filesystem::path& source_file : generated_core_source_files) {
        expected_generated_files.emplace(strex("{}", source_file.filename().string()).str());
    }

    for (const string_view target : targets) {
        vector<std::filesystem::path> source_files;
        unordered_set<string> source_file_keys;
        vector<string> references;
        unordered_set<string> reference_keys;

        const auto append_source_file = [&](const std::filesystem::path& source_file) {
            std::error_code ec;
            const std::filesystem::path canonical_path = std::filesystem::weakly_canonical(source_file, ec);
            const std::filesystem::path normalized_path = ec ? source_file.lexically_normal() : canonical_path;
            const string source_key = strex("{}", normalized_path.string()).str();

            if (source_file_keys.emplace(source_key).second) {
                source_files.emplace_back(normalized_path);
            }
        };

        const auto append_reference = [&](const string& reference) {
            if (reference_keys.emplace(reference).second) {
                references.emplace_back(reference);
            }
        };

        for (const string& assembly : assemblies) {
            for (const std::filesystem::path& source_file : CollectSourceFiles(files, generated_core_source_files, assembly, target)) {
                append_source_file(source_file);
            }
            for (const string& reference : CollectReferences(assembly, target)) {
                append_reference(reference);
            }
        }

        std::ranges::sort(source_files, {}, [](const std::filesystem::path& path) { return path.string(); });
        std::ranges::sort(references);

        const string assembly_res_path = MakeManagedEntryAssemblyResourcePath(managed_pack_name, target);
        const uint64_t managed_bake_stamp = GetManagedBakeStamp(*_context, target, source_files, references);
        const bool should_bake = !_context->BakeChecker || _context->BakeChecker(assembly_res_path, managed_bake_stamp);

        project_sources.emplace(string(target), source_files);
        project_references.emplace(string(target), references);
        target_tasks.emplace_back(TargetBakeTask {
            .Target = string(target),
            .ResourcePath = assembly_res_path,
            .SourceFiles = std::move(source_files),
            .References = std::move(references),
            .ShouldBake = should_bake,
        });
    }

    expected_generated_files.emplace(MakeGeneratedManagedUnifiedProjectFileName(project_name));
    expected_generated_files.emplace(MakeGeneratedManagedSolutionFileName(project_name));
    GenerateUnifiedProjectFile(managed_project_dir, managed_assemblies_output_dir, managed_pack_name, project_name, project_sources, project_references);
    GenerateSolutionFile(managed_project_dir, project_name, vector<string> {project_name});

    for (const string_view target : targets) {
        WriteLog("Generate Managed C# API for {}", target);

        if (target == "Server") {
            const BakerServerEngine engine(*_context->BakedFiles);
            GenerateTargetApiFiles(engine, managed_project_dir, target);
        }
        else if (target == "Client") {
            const BakerClientEngine engine(*_context->BakedFiles);
            GenerateTargetApiFiles(engine, managed_project_dir, target);
        }
        else if (target == "Mapper") {
            const BakerMapperEngine engine(*_context->BakedFiles);
            GenerateTargetApiFiles(engine, managed_project_dir, target);
        }
        else {
            FO_UNREACHABLE_PLACE();
        }
    }

    RemoveStaleGeneratedManagedArtifacts(managed_project_dir, expected_generated_files);

    const std::filesystem::path project_path = managed_project_dir / MakeGeneratedManagedUnifiedProjectFileName(project_name);

    for (const TargetBakeTask& task : target_tasks) {
        if (!task.ShouldBake) {
            continue;
        }

        const string_view target = task.Target;
        const string assembly_file_name = MakeManagedAssemblyFileName(managed_pack_name, target);

        if (dry_run) {
            const string marker = strex("ManagedScriptBaker dry run {} {}", managed_pack_name, target).str();
            _context->WriteData(task.ResourcePath, const_span<uint8_t> {reinterpret_cast<const uint8_t*>(marker.data()), marker.size()});
        }
        else {
            const string msbuild_command = MakeManagedMsBuildCommand(GetEnvValue("FO_MANAGED_MSBUILD"));
            const string output_path = MakeAbsoluteProjectOutputPath(managed_assemblies_output_dir, target);
            const string command = strex("{} -noLogo -nodeReuse:false -m -restore -target:Build -p:Configuration={} -p:Platform=AnyCPU -p:OutputPath=\"{}\" \"{}\"", msbuild_command, target, output_path, project_path.generic_string()).str();
            RemoveManagedOutputAssemblies(managed_assemblies_output_dir, target);
            RemoveManagedBuildSidecars(managed_assemblies_output_dir, target, assembly_file_name);
            RunCommand(command, "ManagedScriptBaker compilation failed");
            RemoveManagedBuildSidecars(managed_assemblies_output_dir, target, assembly_file_name);

            bool entry_assembly_written = false;

            for (const std::filesystem::path& assembly_disk_path : CollectManagedOutputAssemblies(managed_assemblies_output_dir, target)) {
                const string output_file_name = strex("{}", assembly_disk_path.filename().string()).str();
                const string resource_path = MakeManagedOutputAssemblyResourcePath(target, output_file_name);
                const vector<uint8_t> assembly_data = ReadFileBytes(assembly_disk_path);
                _context->WriteData(resource_path, assembly_data);

                if (output_file_name == assembly_file_name) {
                    entry_assembly_written = true;
                }
            }

            if (!entry_assembly_written) {
                throw ManagedScriptBakerException("Managed entry assembly was not produced", assembly_file_name);
            }
        }
    }
}

// ManagedScriptBaker generation steps
void ManagedScriptBaker::GenerateTargetApiFiles(const EngineMetadata& meta, const std::filesystem::path& project_dir, string_view target_name)
{
    FO_STACK_TRACE_ENTRY();

    unordered_map<string, ComplexTypeDesc> callbacks;
    CollectCallbacks(meta, callbacks);

    {
        std::ostringstream out;
        AppendGeneratedHeader(out);

        for (const auto& [enum_name, enum_values] : MakeSortedEnums(meta)) {
            const BaseTypeDesc& enum_type = meta.GetBaseType(enum_name);
            out << "    public enum " << EscapeCsIdentifier(enum_name) << " : " << MakeEnumUnderlyingCsType(enum_type) << "\n";
            out << "    {\n";

            for (const auto& [value_name, value] : enum_values) {
                out << CS_INDENT << EscapeCsIdentifier(value_name) << " = " << value << ",\n";
            }

            out << "    }\n\n";
        }

        AppendGeneratedFooter(out);
        WriteGeneratedFile(project_dir, target_name, "Enums", out.str());
    }

    {
        std::ostringstream out;
        AppendGeneratedHeader(out);
        AppendHstringType(out);

        vector<pair<string, ComplexTypeDesc>> sorted_callbacks;
        sorted_callbacks.reserve(callbacks.size());

        for (const auto& [name, type] : callbacks) {
            sorted_callbacks.emplace_back(name, type);
        }

        std::ranges::sort(sorted_callbacks, {}, [](const auto& entry) -> const string& { return entry.first; });

        for (const auto& [name, type] : sorted_callbacks) {
            FO_VERIFY_AND_THROW(type.CallbackArgs, "Callback type has no argument list");
            const ComplexTypeDesc& ret = type.CallbackArgs->front();
            const_span<ComplexTypeDesc> args = span(*type.CallbackArgs).subspan(1);
            vector<string> arg_declarations;
            arg_declarations.reserve(args.size());

            for (size_t i = 0; i < args.size(); i++) {
                arg_declarations.emplace_back(strex("{} arg{}", MakeCsTypeName(args[i]), i).str());
            }

            AppendCsCallableDeclaration(out, "    ", strex("public delegate {} ", MakeCsTypeName(ret)).str(), EscapeCsIdentifier(name), arg_declarations, ";");
            out << "\n";
        }

        for (const BaseTypeDesc* type : MakeSortedBaseTypes(meta)) {
            if (type->IsStruct && type->StructLayout != nullptr) {
                out << "    public partial struct " << EscapeCsIdentifier(type->Name) << "\n";
                out << "    {\n";

                vector<string> ctor_args;
                size_t ctor_args_length = 0;

                for (const FieldDesc& field : type->StructLayout->Fields) {
                    const string field_name = EscapeCsIdentifier(field.Name);
                    const string field_type = MakeCsTypeName(field.Type);

                    out << CS_INDENT << "public " << field_type << " " << field_name << ";\n";

                    string ctor_arg = strex("{} {}", field_type, field_name).str();
                    ctor_args_length += ctor_arg.length();
                    ctor_args.emplace_back(std::move(ctor_arg));
                }

                if (!type->StructLayout->Fields.empty()) {
                    out << "\n";

                    if (ctor_args.size() > 4 || ctor_args_length > 100) {
                        out << CS_INDENT << "public " << EscapeCsIdentifier(type->Name) << "(\n";

                        for (size_t i = 0; i < ctor_args.size(); i++) {
                            out << CS_INDENT << "    " << ctor_args[i] << (i + 1 != ctor_args.size() ? "," : "") << "\n";
                        }

                        out << CS_INDENT << ")\n";
                    }
                    else {
                        string ctor_args_line;

                        for (const string& ctor_arg : ctor_args) {
                            if (!ctor_args_line.empty()) {
                                ctor_args_line += ", ";
                            }

                            ctor_args_line += ctor_arg;
                        }

                        out << CS_INDENT << "public " << EscapeCsIdentifier(type->Name) << "(" << ctor_args_line << ")\n";
                    }

                    out << CS_INDENT << "{\n";

                    for (const FieldDesc& field : type->StructLayout->Fields) {
                        const string field_name = EscapeCsIdentifier(field.Name);
                        out << CS_INDENT << "    this." << field_name << " = " << field_name << ";\n";
                    }

                    out << CS_INDENT << "}\n";
                }

                if (!type->StructLayout->Fields.empty()) {
                    const string struct_name = EscapeCsIdentifier(type->Name);
                    string eq_expr;
                    string hash_args;

                    for (const FieldDesc& field : type->StructLayout->Fields) {
                        const string field_name = EscapeCsIdentifier(field.Name);

                        if (!eq_expr.empty()) {
                            eq_expr += " && ";
                            hash_args += ", ";
                        }

                        eq_expr += strex("left.{} == right.{}", field_name, field_name).str();
                        hash_args += field_name;
                    }

                    out << "\n";
                    out << CS_INDENT << "public static bool operator ==(" << struct_name << " left, " << struct_name << " right)\n";
                    out << CS_INDENT << "{\n";
                    out << CS_INDENT << "    return " << eq_expr << ";\n";
                    out << CS_INDENT << "}\n\n";
                    out << CS_INDENT << "public static bool operator !=(" << struct_name << " left, " << struct_name << " right)\n";
                    out << CS_INDENT << "{\n";
                    out << CS_INDENT << "    return !(left == right);\n";
                    out << CS_INDENT << "}\n\n";
                    out << CS_INDENT << "public override bool Equals(object? obj)\n";
                    out << CS_INDENT << "{\n";
                    out << CS_INDENT << "    return obj is " << struct_name << " other && this == other;\n";
                    out << CS_INDENT << "}\n\n";
                    out << CS_INDENT << "public override int GetHashCode()\n";
                    out << CS_INDENT << "{\n";

                    // System.HashCode.Combine only provides overloads for up to eight values;
                    // wider structs accumulate the hash incrementally to stay within that limit.
                    if (type->StructLayout->Fields.size() <= 8) {
                        out << CS_INDENT << "    return System.HashCode.Combine(" << hash_args << ");\n";
                    }
                    else {
                        out << CS_INDENT << "    var hash = new System.HashCode();\n";

                        for (const FieldDesc& field : type->StructLayout->Fields) {
                            out << CS_INDENT << "    hash.Add(" << EscapeCsIdentifier(field.Name) << ");\n";
                        }

                        out << CS_INDENT << "    return hash.ToHashCode();\n";
                    }

                    out << CS_INDENT << "}\n";
                }

                out << "    }\n\n";
            }
            else if (type->IsRefType && type->RefType != nullptr) {
                unordered_set<string> member_names;

                out << "    public partial class " << EscapeCsIdentifier(type->Name) << "\n";
                out << "    {\n";

                if (type->RefType->FieldsRegistrator != nullptr) {
                    out << CS_INDENT << "public " << EscapeCsIdentifier(type->Name) << "()\n";
                    out << CS_INDENT << "{\n";
                    out << CS_INDENT << "}\n\n";
                    // A RefType/ExportObject DTO is a standalone managed class (no Entity base), so its
                    // Name/Id/ProtoId/IsDestroyed properties shadow nothing -> no `new` (CS0109 otherwise).
                    AppendEntityProperties(out, type->RefType->FieldsRegistrator.get(), type->Name, target_name, {}, false, false, true, false, member_names);
                }
                else {
                    out << CS_INDENT << "private IntPtr _refPtr;\n\n";
                    out << CS_INDENT << "public " << EscapeCsIdentifier(type->Name) << "()\n";
                    out << CS_INDENT << "{\n";
                    out << CS_INDENT << "    _refPtr = IntPtr.Zero;\n";
                    out << CS_INDENT << "}\n\n";
                    out << CS_INDENT << "internal " << EscapeCsIdentifier(type->Name) << "(IntPtr refPtr)\n";
                    out << CS_INDENT << "{\n";
                    out << CS_INDENT << "    _refPtr = refPtr;\n";
                    out << CS_INDENT << "}\n\n";
                    out << CS_INDENT << "internal IntPtr RefPtr\n";
                    out << CS_INDENT << "{\n";
                    out << CS_INDENT << "    get\n";
                    out << CS_INDENT << "    {\n";
                    out << CS_INDENT << "        return _refPtr;\n";
                    out << CS_INDENT << "    }\n";
                    out << CS_INDENT << "}\n\n";
                }

                AppendMethods(out, type->RefType->Methods, type->Name, target_name, false, type->RefType->FieldsRegistrator == nullptr, type->RefType->FieldsRegistrator == nullptr, member_names);
                out << "    }\n\n";
            }
        }

        AppendGeneratedFooter(out);
        WriteGeneratedFile(project_dir, target_name, "Types", out.str());
    }

    {
        std::ostringstream out;
        AppendGeneratedHeader(out);
        AppendEntityBaseClass(out);

        for (const auto& [type_name, desc] : MakeSortedEntityTypes(meta.GetEntityTypes())) {
            AppendComponentClasses(out, type_name, *desc, target_name);
        }
        for (const auto& [type_name, desc] : MakeSortedEntityTypes(meta.GetFixedTypes())) {
            AppendComponentClasses(out, type_name, *desc, target_name);
        }

        for (const auto& [type_name, desc] : MakeSortedEntityTypes(meta.GetEntityTypes())) {
            if (type_name != "Game" && desc->HasAbstract) {
                // Mirror the AngelScript cast graph: `Abstract<Type> : Entity` carries the content, and the concrete
                // `<Type>` plus `Proto<Type>`/`Static<Type>` derive from it (so a `<Type>@` IS-A `Abstract<Type>`).
                // The content's native lookups + component names stay keyed to the concrete `<Type>` via native_owner.
                const string abstract_name = strex("Abstract{}", type_name).str();
                AppendEntityClass(out, abstract_name, "Entity", *desc, target_name, type_name);
                AppendEmptyDerivedEntity(out, type_name, abstract_name);

                if (desc->HasProtos) {
                    AppendEmptyDerivedEntity(out, strex("Proto{}", type_name).str(), abstract_name);
                }
                if (desc->HasStatics) {
                    AppendEmptyDerivedEntity(out, strex("Static{}", type_name).str(), abstract_name);
                }
            }
            else {
                AppendEntityClass(out, type_name, "Entity", *desc, target_name);

                if (type_name != "Game") {
                    if (desc->HasProtos) {
                        AppendEmptyDerivedEntity(out, strex("Proto{}", type_name).str(), type_name);
                    }
                    if (desc->HasStatics) {
                        AppendEmptyDerivedEntity(out, strex("Static{}", type_name).str(), type_name);
                    }
                }
            }
        }

        for (const auto& [type_name, desc] : MakeSortedEntityTypes(meta.GetFixedTypes())) {
            AppendEntityClass(out, type_name, "Entity", *desc, target_name, {}, true);
        }

        AppendCustomEntityProtoGetters(out, meta);
        AppendPropertyGroupGetters(out, meta);
        AppendPropertyInfoAccessors(out, meta);
        AppendRemoteCallerSurface(out, meta, target_name);
        AppendPropertyCallbackRegistrars(out, meta, target_name);

        AppendGeneratedFooter(out);
        WriteGeneratedFile(project_dir, target_name, "Entities", out.str());
    }

    {
        std::ostringstream out;
        AppendGeneratedHeader(out);

        for (const auto& [type_name, desc] : MakeSortedEntityTypes(meta.GetEntityTypes())) {
            for (const EntityEventDesc& event : desc->Events) {
                const string event_type_name = strex("{}{}Event", type_name, event.Name).str();
                const vector<string> event_arg_declarations = MakeCsEventArgumentDeclarations(type_name, desc->IsGlobal, event.Args);
                const string delegate_name = strex("{}{}EventHandler", type_name, event.Name).str();
                const string result_delegate_name = strex("{}{}EventHandlerResult", type_name, event.Name).str();
                const string target_literal = EscapeCsStringLiteral(target_name);
                const string owner_literal = EscapeCsStringLiteral(type_name);
                const string event_literal = EscapeCsStringLiteral(event.Name);

                AppendCsCallableDeclaration(out, "    ", "public delegate void ", EscapeCsIdentifier(delegate_name), event_arg_declarations, ";");
                AppendCsCallableDeclaration(out, "    ", "public delegate EventResult ", EscapeCsIdentifier(result_delegate_name), event_arg_declarations, ";");
                out << "\n";
                out << "    public sealed class " << EscapeCsIdentifier(event_type_name) << "\n";
                out << "    {\n";
                out << CS_INDENT << "private readonly IntPtr _entityPtr;\n";
                out << CS_INDENT << "private readonly Dictionary<(Delegate Handler, IntPtr Backend), IntPtr> _nativeSubscriptions =\n";
                out << CS_INDENT << "    new Dictionary<(Delegate Handler, IntPtr Backend), IntPtr>();\n";
                out << "\n";
                out << CS_INDENT << "internal " << EscapeCsIdentifier(event_type_name) << "(IntPtr entityPtr)\n";
                out << CS_INDENT << "{\n";
                out << CS_INDENT << "    _entityPtr = entityPtr;\n";
                out << CS_INDENT << "}\n\n";
                AppendCsCallableDeclaration(out, CS_INDENT, "public void ", "Subscribe", vector<string> {strex("{} handler", EscapeCsIdentifier(delegate_name)).str(), "EventPriority priority = EventPriority.Normal"}, "");
                out << CS_INDENT << "{\n";
                out << CS_INDENT << "    if (handler == null)\n";
                out << CS_INDENT << "    {\n";
                out << CS_INDENT << "        return;\n";
                out << CS_INDENT << "    }\n\n";
                out << CS_INDENT << "    global::FOnline.Native.RequireEventAttribute(handler);\n";
                out << CS_INDENT << "    IntPtr backend = global::FOnline.Native.GetBackend(\"" << target_literal << "\");\n";
                out << CS_INDENT << "    (Delegate Handler, IntPtr Backend) key = ((Delegate)handler, backend);\n";
                out << CS_INDENT << "    if (_nativeSubscriptions.ContainsKey(key))\n";
                out << CS_INDENT << "    {\n";
                out << CS_INDENT << "        return;\n";
                out << CS_INDENT << "    }\n\n";
                out << CS_INDENT << "    _nativeSubscriptions[key] = global::FOnline.Native.SubscribeEvent(\n";
                out << CS_INDENT << "        \"" << target_literal << "\",\n";
                out << CS_INDENT << "        \"" << owner_literal << "\",\n";
                out << CS_INDENT << "        \"" << event_literal << "\",\n";
                out << CS_INDENT << "        _entityPtr,\n";
                out << CS_INDENT << "        handler,\n";
                out << CS_INDENT << "        false,\n";
                out << CS_INDENT << "        (int)priority);\n";
                out << CS_INDENT << "}\n\n";
                AppendCsCallableDeclaration(out, CS_INDENT, "public void ", "Subscribe", vector<string> {strex("{} handler", EscapeCsIdentifier(result_delegate_name)).str(), "EventPriority priority = EventPriority.Normal"}, "");
                out << CS_INDENT << "{\n";
                out << CS_INDENT << "    if (handler == null)\n";
                out << CS_INDENT << "    {\n";
                out << CS_INDENT << "        return;\n";
                out << CS_INDENT << "    }\n\n";
                out << CS_INDENT << "    global::FOnline.Native.RequireEventAttribute(handler);\n";
                out << CS_INDENT << "    IntPtr backend = global::FOnline.Native.GetBackend(\"" << target_literal << "\");\n";
                out << CS_INDENT << "    (Delegate Handler, IntPtr Backend) key = ((Delegate)handler, backend);\n";
                out << CS_INDENT << "    if (_nativeSubscriptions.ContainsKey(key))\n";
                out << CS_INDENT << "    {\n";
                out << CS_INDENT << "        return;\n";
                out << CS_INDENT << "    }\n\n";
                out << CS_INDENT << "    _nativeSubscriptions[key] = global::FOnline.Native.SubscribeEvent(\n";
                out << CS_INDENT << "        \"" << target_literal << "\",\n";
                out << CS_INDENT << "        \"" << owner_literal << "\",\n";
                out << CS_INDENT << "        \"" << event_literal << "\",\n";
                out << CS_INDENT << "        _entityPtr,\n";
                out << CS_INDENT << "        handler,\n";
                out << CS_INDENT << "        true,\n";
                out << CS_INDENT << "        (int)priority);\n";
                out << CS_INDENT << "}\n\n";
                AppendCsCallableDeclaration(out, CS_INDENT, "public void ", "Unsubscribe", vector<string> {strex("{} handler", EscapeCsIdentifier(delegate_name)).str()}, "");
                out << CS_INDENT << "{\n";
                out << CS_INDENT << "    if (handler == null)\n";
                out << CS_INDENT << "    {\n";
                out << CS_INDENT << "        return;\n";
                out << CS_INDENT << "    }\n\n";
                out << CS_INDENT << "    IntPtr backend = global::FOnline.Native.GetBackend(\"" << target_literal << "\");\n";
                out << CS_INDENT << "    (Delegate Handler, IntPtr Backend) key = ((Delegate)handler, backend);\n";
                out << CS_INDENT << "    if (_nativeSubscriptions.TryGetValue(key, out IntPtr subscription))\n";
                out << CS_INDENT << "    {\n";
                out << CS_INDENT << "        global::FOnline.Native.UnsubscribeEvent(\n";
                out << CS_INDENT << "            \"" << target_literal << "\",\n";
                out << CS_INDENT << "            \"" << event_literal << "\",\n";
                out << CS_INDENT << "            _entityPtr,\n";
                out << CS_INDENT << "            subscription);\n";
                out << CS_INDENT << "        _nativeSubscriptions.Remove(key);\n";
                out << CS_INDENT << "    }\n";
                out << CS_INDENT << "}\n\n";
                AppendCsCallableDeclaration(out, CS_INDENT, "public void ", "Unsubscribe", vector<string> {strex("{} handler", EscapeCsIdentifier(result_delegate_name)).str()}, "");
                out << CS_INDENT << "{\n";
                out << CS_INDENT << "    if (handler == null)\n";
                out << CS_INDENT << "    {\n";
                out << CS_INDENT << "        return;\n";
                out << CS_INDENT << "    }\n\n";
                out << CS_INDENT << "    IntPtr backend = global::FOnline.Native.GetBackend(\"" << target_literal << "\");\n";
                out << CS_INDENT << "    (Delegate Handler, IntPtr Backend) key = ((Delegate)handler, backend);\n";
                out << CS_INDENT << "    if (_nativeSubscriptions.TryGetValue(key, out IntPtr subscription))\n";
                out << CS_INDENT << "    {\n";
                out << CS_INDENT << "        global::FOnline.Native.UnsubscribeEvent(\n";
                out << CS_INDENT << "            \"" << target_literal << "\",\n";
                out << CS_INDENT << "            \"" << event_literal << "\",\n";
                out << CS_INDENT << "            _entityPtr,\n";
                out << CS_INDENT << "            subscription);\n";
                out << CS_INDENT << "        _nativeSubscriptions.Remove(key);\n";
                out << CS_INDENT << "    }\n";
                out << CS_INDENT << "}\n\n";
                out << CS_INDENT << "public void UnsubscribeAll()\n";
                out << CS_INDENT << "{\n";
                out << CS_INDENT << "    IntPtr backend = global::FOnline.Native.GetBackend(\"" << target_literal << "\");\n";
                out << CS_INDENT << "    List<(Delegate Handler, IntPtr Backend)> keys = new List<(Delegate Handler, IntPtr Backend)>();\n\n";
                out << CS_INDENT << "    foreach (KeyValuePair<(Delegate Handler, IntPtr Backend), IntPtr> entry in _nativeSubscriptions)\n";
                out << CS_INDENT << "    {\n";
                out << CS_INDENT << "        if (entry.Key.Backend == backend)\n";
                out << CS_INDENT << "        {\n";
                out << CS_INDENT << "            keys.Add(entry.Key);\n";
                out << CS_INDENT << "        }\n";
                out << CS_INDENT << "    }\n\n";
                out << CS_INDENT << "    foreach ((Delegate Handler, IntPtr Backend) key in keys)\n";
                out << CS_INDENT << "    {\n";
                out << CS_INDENT << "        global::FOnline.Native.UnsubscribeEvent(\n";
                out << CS_INDENT << "            \"" << target_literal << "\",\n";
                out << CS_INDENT << "            \"" << event_literal << "\",\n";
                out << CS_INDENT << "            _entityPtr,\n";
                out << CS_INDENT << "            _nativeSubscriptions[key]);\n";
                out << CS_INDENT << "        _nativeSubscriptions.Remove(key);\n";
                out << CS_INDENT << "    }\n\n";
                out << CS_INDENT << "}\n";

                if (!event.Exported) {
                    out << "\n";
                    AppendCsCallableDeclaration(out, CS_INDENT, "public EventResult ", "Fire", MakeCsArgumentDeclarations(event.Args), "");
                    out << CS_INDENT << "{\n";
                    out << CS_INDENT << "    try\n";
                    out << CS_INDENT << "    {\n";
                    AppendObjectArrayDeclaration(out, strex("{}        ", CS_INDENT).str(), "__args", event.Args);
                    out << CS_INDENT << "        EventResult __result = (EventResult)global::FOnline.Native.FireEvent(\n";
                    out << CS_INDENT << "            \"" << target_literal << "\",\n";
                    out << CS_INDENT << "            \"" << owner_literal << "\",\n";
                    out << CS_INDENT << "            \"" << event_literal << "\",\n";
                    out << CS_INDENT << "            _entityPtr,\n";
                    out << CS_INDENT << "            __args);\n";

                    if (HasMutableArgs(event.Args)) {
                        AppendMutableArgAssignments(out, event.Args, "__args", 0);
                    }

                    out << CS_INDENT << "        return __result;\n";
                    out << CS_INDENT << "    }\n";
                    out << CS_INDENT << "    catch (Exception ex)\n";
                    out << CS_INDENT << "    {\n";
                    out << CS_INDENT << "        global::FOnline.Game.RecordManagedException(ex, true);\n";
                    out << CS_INDENT << "        return EventResult.StopChain;\n";
                    out << CS_INDENT << "    }\n";
                    out << CS_INDENT << "}\n";
                }

                out << "    }\n\n";
            }
        }

        AppendGeneratedFooter(out);
        WriteGeneratedFile(project_dir, target_name, "Events", out.str());
    }

    {
        std::ostringstream out;
        AppendGeneratedHeader(out);
        out << "    public static partial class Settings\n";
        out << "    {\n";

        unordered_set<string> setting_names;
        map<string, const BaseTypeDesc*> sorted_settings;

        for (const auto& [setting_name, setting_type] : meta.GetGameSettings()) {
            sorted_settings.emplace(setting_name, setting_type.get());
        }

        for (const auto& [setting_name, setting_type] : sorted_settings) {
            string accessor_name = setting_name;
            std::ranges::replace(accessor_name, '.', '_');
            AppendSettingProperty(out, ComplexTypeDesc {.Kind = ComplexTypeKind::Simple, .BaseType = *setting_type}, EscapeCsIdentifier(accessor_name), target_name, setting_name, setting_names);
        }

        // Engine ExportSettings (View/Hex/Input/...) are not in GetGameSettings() -- the metadata blob's "Setting"
        // section carries only project `///@ Setting` tags -- so pull them from the codegen-generated per-target
        // typed list. Mapper's list includes the Client/Common engine setting groups because mapper AngelScript
        // exposes the same visual/input/geometry settings. `setting_names` dedups against project settings.
        vector<pair<string, string>> export_settings;

        if (target_name == "Server") {
            export_settings = GetServerSettingsTyped();
        }
        else if (target_name == "Client") {
            export_settings = GetClientSettingsTyped();
        }
        else if (target_name == "Mapper") {
            export_settings = GetMapperSettingsTyped();
        }

        for (const auto& [setting_name, type_name] : export_settings) {
            string accessor_name = setting_name;
            std::ranges::replace(accessor_name, '.', '_');
            const ComplexTypeDesc setting_type = meta.ResolveComplexType(type_name);
            AppendSettingProperty(out, setting_type, EscapeCsIdentifier(accessor_name), target_name, setting_name, setting_names);
        }

        out << "    }\n";
        AppendGeneratedFooter(out);
        WriteGeneratedFile(project_dir, target_name, "Settings", out.str());
    }
}

void ManagedScriptBaker::GenerateUnifiedProjectFile(const std::filesystem::path& project_dir, const std::filesystem::path& assemblies_output_dir, string_view pack_name, string_view project_name, const map<string, vector<std::filesystem::path>>& source_files, const map<string, vector<string>>& references)
{
    FO_STACK_TRACE_ENTRY();

    const string proj_name = MakeGeneratedManagedUnifiedProjectFileName(project_name);
    const std::filesystem::path proj_path = project_dir / proj_name;
    const string target_framework = GetManagedTargetFramework();

    std::ostringstream file;
    const array<string_view, 3> targets {"Server", "Client", "Mapper"};
    const array<string_view, 5> generated_files {"Enums", "Types", "Entities", "Events", "Settings"};

    file << GENERATED_XML_DISCLAIMER;
    file << "<Project Sdk=\"Microsoft.NET.Sdk\">\n";
    file << "  <PropertyGroup>\n";
    file << "    <Configuration Condition=\" '$(Configuration)' == '' \">Server</Configuration>\n";
    file << "    <Platform Condition=\" '$(Platform)' == '' \">AnyCPU</Platform>\n";
    file << "    <Configurations>Server;Client;Mapper</Configurations>\n";
    file << "    <RootNamespace>" << EscapeXml(project_name) << "</RootNamespace>\n";
    file << "    <OutputType>Library</OutputType>\n";
    file << "    <TargetFramework>" << EscapeXml(target_framework) << "</TargetFramework>\n";
    file << "    <EnableDefaultItems>false</EnableDefaultItems>\n";
    file << "    <EnableDefaultCompileItems>false</EnableDefaultCompileItems>\n";
    file << "    <ImplicitUsings>false</ImplicitUsings>\n";
    file << "    <Nullable>enable</Nullable>\n";
    file << "    <LangVersion>latest</LangVersion>\n";
    file << "    <TreatWarningsAsErrors>true</TreatWarningsAsErrors>\n";
    file << "    <NoWarn>$(NoWarn);8981</NoWarn>\n";
    file << "    <GenerateDependencyFile>false</GenerateDependencyFile>\n";
    file << "    <AppendTargetFrameworkToOutputPath>false</AppendTargetFrameworkToOutputPath>\n";
    file << "    <AppendRuntimeIdentifierToOutputPath>false</AppendRuntimeIdentifierToOutputPath>\n";
    file << "  </PropertyGroup>\n";

    for (const string_view target : targets) {
        const string output_path = MakeProjectOutputPath(project_dir, assemblies_output_dir, target);

        file << "  <PropertyGroup Condition=\" '$(Configuration)|$(Platform)' == '" << EscapeXml(target) << "|AnyCPU' \">\n";
        file << "    <DebugType>embedded</DebugType>\n";
        file << "    <Optimize>true</Optimize>\n";
        file << "    <AssemblyName>" << EscapeXml(pack_name) << "." << EscapeXml(target) << "</AssemblyName>\n";
        file << "    <OutputPath>" << EscapeXml(output_path) << "</OutputPath>\n";
        file << "    <DefineConstants>TRACE;" << strex(target).upper().str() << "</DefineConstants>\n";
        file << "  </PropertyGroup>\n";
    }

    for (const string_view target : targets) {
        const string condition = strex(" '$(Configuration)' == '{}' ", target).str();
        const string_view condition_view = condition;

        file << "  <ItemGroup Condition=\"" << EscapeXml(condition) << "\">\n";

        for (const string_view file_suffix : generated_files) {
            file << "    <Compile Include=\"" << EscapeXml(MakeGeneratedManagedApiFileName(target, file_suffix)) << "\" />\n";
        }

        if (const auto it = source_files.find(string(target)); it != source_files.end()) {
            for (const std::filesystem::path& source_file : it->second) {
                file << "    <Compile Include=\"" << EscapeXml(MakeRelativeProjectPath(project_dir, source_file)) << "\" />\n";
            }
        }

        file << "  </ItemGroup>\n";

        if (const auto it = references.find(string(target)); it != references.end()) {
            AppendProjectReferences(file, project_dir, it->second, condition_view);
        }
    }

    file << "</Project>\n";
    WriteTextFileIfChanged(proj_path, file.str(), "Can't create generated project file");

    std::error_code ec;
    std::filesystem::remove(project_dir / strex("{}.csproj", project_name).str(), ec);
}

void ManagedScriptBaker::GenerateSolutionFile(const std::filesystem::path& project_dir, string_view solution_name, const vector<string>& project_names)
{
    FO_STACK_TRACE_ENTRY();

    const string sln_name = MakeGeneratedManagedSolutionFileName(solution_name);
    const std::filesystem::path sln_path = project_dir / sln_name;

    std::ostringstream file;
    constexpr string_view CSHARP_PROJECT_TYPE_GUID = "{FAE04EC0-301F-11D3-BF4B-00C04F79EFBC}";
    constexpr string_view SOLUTION_PLATFORM = "AnyCPU";
    const array<string_view, 3> targets {"Server", "Client", "Mapper"};

    vector<string> project_guids;
    project_guids.reserve(project_names.size());

    file << "\xEF\xBB\xBF";
    file << "Microsoft Visual Studio Solution File, Format Version 12.00\r\n";
    file << GENERATED_SOLUTION_DISCLAIMER;
    file << "# Visual Studio Version 17\r\n";
    file << "VisualStudioVersion = 17.0.31903.59\r\n";
    file << "MinimumVisualStudioVersion = 10.0.40219.1\r\n";

    for (const string& project_name : project_names) {
        const string project_guid = MakeManagedSolutionGuid(strex("ManagedProject:{}", project_name).str());
        const std::filesystem::path project_path = project_dir / MakeGeneratedManagedUnifiedProjectFileName(project_name);
        project_guids.emplace_back(project_guid);

        file << "Project(\"" << CSHARP_PROJECT_TYPE_GUID << "\") = \"" << EscapeSolutionString(project_name) << "\", \"" << EscapeSolutionString(MakeSolutionProjectPath(project_dir, project_path)) << "\", \"" << project_guid << "\"\r\n";
        file << "EndProject\r\n";
    }

    file << "Global\r\n";
    file << "\tGlobalSection(SolutionConfigurationPlatforms) = preSolution\r\n";

    for (const string_view target : targets) {
        file << "\t\t" << target << "|" << SOLUTION_PLATFORM << " = " << target << "|" << SOLUTION_PLATFORM << "\r\n";
    }

    file << "\tEndGlobalSection\r\n";
    file << "\tGlobalSection(ProjectConfigurationPlatforms) = postSolution\r\n";

    for (const string& project_guid : project_guids) {
        for (const string_view target : targets) {
            file << "\t\t" << project_guid << "." << target << "|" << SOLUTION_PLATFORM << ".ActiveCfg = " << target << "|" << SOLUTION_PLATFORM << "\r\n";
            file << "\t\t" << project_guid << "." << target << "|" << SOLUTION_PLATFORM << ".Build.0 = " << target << "|" << SOLUTION_PLATFORM << "\r\n";
        }
    }

    file << "\tEndGlobalSection\r\n";
    file << "\tGlobalSection(SolutionProperties) = preSolution\r\n";
    file << "\t\tHideSolutionNode = FALSE\r\n";
    file << "\tEndGlobalSection\r\n";
    file << "EndGlobal\r\n";
    WriteTextFileIfChanged(sln_path, file.str(), "Can't create generated solution file");

    std::error_code ec;
    std::filesystem::remove(project_dir / strex("{}.sln", solution_name).str(), ec);
}

auto ManagedScriptBaker::CollectSourceFiles(const FileCollection& files, const vector<std::filesystem::path>& generated_core_source_files, string_view assembly_name, string_view target_name) -> vector<std::filesystem::path>
{
    FO_STACK_TRACE_ENTRY();

    vector<std::filesystem::path> result;
    unordered_set<string> unique_paths;

    const auto add_source = [&](const std::filesystem::path& path) {
        std::error_code ec;
        const std::filesystem::path normalized_path = std::filesystem::weakly_canonical(path, ec);
        const string key = strex("{}", (ec ? path.lexically_normal() : normalized_path).string()).str();

        if (unique_paths.emplace(key).second) {
            result.emplace_back(ec ? path.lexically_normal() : normalized_path);
        }
    };

    for (const std::filesystem::path& source_file : generated_core_source_files) {
        add_source(source_file);
    }

    for (const FileHeader& file : files) {
        if (strex(file.GetPath()).get_file_extension() != "cs") {
            continue;
        }
        if (!file.GetDataSource()->IsDiskDir()) {
            continue;
        }

        const std::filesystem::path disk_path = file.GetDiskPath();

        if (IsGeneratedManagedArtifactFileName(strex("{}", disk_path.filename().string()).str())) {
            continue;
        }
        if (IsGeneratedManagedApiFile(disk_path)) {
            continue;
        }

        add_source(disk_path);
    }

    for (const string& source_path : CollectScopedEnvValues(GetEnvValue("FO_MANAGED_SOURCE"), assembly_name, target_name)) {
        const std::filesystem::path source_file_path = source_path;

        if (IsGeneratedManagedArtifactFileName(strex("{}", source_file_path.filename().string()).str())) {
            continue;
        }
        if (IsGeneratedManagedApiFile(source_file_path)) {
            continue;
        }

        add_source(source_file_path);
    }

    std::ranges::sort(result, {}, [](const std::filesystem::path& path) { return path.string(); });
    return result;
}

auto ManagedScriptBaker::CollectReferences(string_view assembly_name, string_view target_name) -> vector<string>
{
    FO_STACK_TRACE_ENTRY();

    vector<string> result;
    unordered_set<string> unique_refs(result.begin(), result.end());

    for (const string& reference : CollectScopedEnvValues(GetEnvValue("FO_MANAGED_REFERENCES"), assembly_name, target_name)) {
        if (unique_refs.emplace(reference).second) {
            result.emplace_back(reference);
        }
    }

    std::ranges::sort(result);
    return result;
}

