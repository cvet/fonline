static auto IsIdentifierStart(char c) noexcept -> bool
{
    FO_NO_STACK_TRACE_ENTRY();

    return std::isalpha(static_cast<unsigned char>(c)) != 0 || c == '_';
}

static auto IsIdentifierPart(char c) noexcept -> bool
{
    FO_NO_STACK_TRACE_ENTRY();

    return std::isalnum(static_cast<unsigned char>(c)) != 0 || c == '_';
}

static auto EscapeCsIdentifier(string_view name) -> string
{
    FO_STACK_TRACE_ENTRY();

    string result;
    result.reserve(name.size() + 1);

    for (const char c : name) {
        result.push_back(IsIdentifierPart(c) ? c : '_');
    }

    if (result.empty() || !IsIdentifierStart(result.front())) {
        result.insert(result.begin(), '_');
    }

    if (IsCsKeyword(result)) {
        result.insert(result.begin(), '@');
    }

    return result;
}

static auto MakeUniqueCsIdentifier(string_view name, unordered_set<string>& used_names) -> string
{
    FO_STACK_TRACE_ENTRY();

    string result = EscapeCsIdentifier(name);

    if (used_names.emplace(result).second) {
        return result;
    }

    for (int32_t suffix = 2;; suffix++) {
        string candidate = strex("{}{}", result, suffix).str();

        if (used_names.emplace(candidate).second) {
            return candidate;
        }
    }
}

static auto EscapeXml(string_view value) -> string
{
    FO_STACK_TRACE_ENTRY();

    string result;
    result.reserve(value.size());

    for (const char c : value) {
        switch (c) {
        case '&':
            result += "&amp;";
            break;
        case '<':
            result += "&lt;";
            break;
        case '>':
            result += "&gt;";
            break;
        case '"':
            result += "&quot;";
            break;
        case '\'':
            result += "&apos;";
            break;
        default:
            result.push_back(c);
            break;
        }
    }

    return result;
}

