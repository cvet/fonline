// Emits Game.SetPropertyGetter / Game.AddPropertySetter overloads, one group per non-global entity
// type, so scripts can register managed virtual-property callbacks the same way AngelScript does.
// The getter is a single generic overload (T is inferred from the getter's return). C# cannot infer a
// type parameter that appears only in a by-ref parameter of a method group, so the setter is emitted
// Typed outbound remote-call surface, mirroring AngelScript's RemoteCaller: player.ServerCall.X(...) on the client,
// player.ClientCall.X(...) and critter.PlayerClientCall.X(...) on the server. One method per outbound remote call
// (BOTH managed "cs" AND AngelScript-defined "fos"): each boxes its args and sends them through the generic
// Native.SendRemoteCall, which serializes with the shared RemoteCallWire format (RemoteCallWire.h) -- the SAME
// byte format the AngelScript backend reads, so a managed caller can invoke an AngelScript-handled server RPC over
// the wire (required for the managed client cutover, where the managed client replaces the AngelScript one). The
// runtime `NativeSendRemoteCall` must allow non-"cs" outbound for this to fire (the conservative cs-only guard is
// lifted in lockstep).
static void AppendRemoteCallerSurface(std::ostringstream& out, const EngineMetadata& meta, string_view target_name)
{
    FO_STACK_TRACE_ENTRY();

    vector<const RemoteCallDesc*> cs_calls;

    for (const auto& call : (*meta.GetOutboundRemoteCalls()) | std::views::values) {
        cs_calls.emplace_back(&call);
    }

    if (cs_calls.empty()) {
        return;
    }

    std::ranges::sort(cs_calls, {}, [](const RemoteCallDesc* call) { return call->Name.as_str(); });

    out << "    public readonly struct RemoteCaller\n";
    out << "    {\n";
    out << CS_INDENT << "private readonly Entity _caller;\n";
    out << "\n";
    out << CS_INDENT << "public RemoteCaller(Entity caller)\n";
    out << CS_INDENT << "{\n";
    out << CS_INDENT << "    _caller = caller;\n";
    out << CS_INDENT << "}\n";

    for (const RemoteCallDesc* call : cs_calls) {
        const string name = string(call->Name.as_str());
        const vector<string> arg_decls = MakeCsArgumentDeclarations(call->Args);
        const vector<string> arg_names = MakeCsArgumentNames(call->Args);
        const string args_array = arg_names.empty() ? "new object[0]" : strex("new object[] {{ {} }}", JoinCsCommaList(arg_names)).str();

        out << "\n";
        out << CS_INDENT << "public void " << EscapeCsIdentifier(name) << "(" << JoinCsCommaList(arg_decls) << ")\n";
        out << CS_INDENT << "{\n";
        out << CS_INDENT << "    global::FOnline.Native.SendRemoteCall(_caller, \"" << name << "\", " << args_array << ");\n";
        out << CS_INDENT << "}\n";
    }

    out << "    }\n\n";

    if (target_name == "Server") {
        out << "    public partial class Player\n";
        out << "    {\n";
        out << CS_INDENT << "public RemoteCaller ClientCall => new RemoteCaller(this);\n";
        out << "    }\n\n";
        out << "    public partial class Critter\n";
        out << "    {\n";
        out << CS_INDENT << "public RemoteCaller PlayerClientCall => new RemoteCaller(this);\n";
        out << "    }\n\n";
    }
    else {
        out << "    public partial class Player\n";
        out << "    {\n";
        out << CS_INDENT << "public RemoteCaller ServerCall => new RemoteCaller(this);\n";
        out << "    }\n\n";
    }
}

// as one non-generic overload per distinct simple property value type; overload resolution then picks
// the match from the supplied setter method group with no explicit type argument.
static void AppendPropertyCallbackRegistrars(std::ostringstream& out, const EngineMetadata& meta, string_view target_name)
{
    FO_STACK_TRACE_ENTRY();

    unordered_set<string> enum_names;

    for (const auto& enum_entry : MakeSortedEnums(meta)) {
        enum_names.emplace(enum_entry.first);
    }

    out << "    public static partial class Game\n";
    out << "    {\n";

    for (const auto& [type_name, desc] : MakeSortedEntityTypes(meta.GetEntityTypes())) {
        if (desc->IsGlobal) {
            continue;
        }

        const string property_enum_name = strex("{}Property", type_name).str();

        if (!enum_names.contains(property_enum_name)) {
            continue;
        }

        nptr<const PropertyRegistrator> registrator = meta.GetPropertyRegistrator(type_name);

        if (!registrator) {
            continue;
        }

        const string entity_type = EscapeCsIdentifier(type_name);
        const string enum_type = EscapeCsIdentifier(property_enum_name);

        out << CS_INDENT << "public static void SetPropertyGetter<T>(" << enum_type << " property, global::System.Func<" << entity_type << ", T> getter)\n";
        out << CS_INDENT << "{\n";
        out << CS_INDENT << "    global::FOnline.Native.SetPropertyGetter(\"" << target_name << "\", \"" << type_name << "\", property.ToString(), getter);\n";
        out << CS_INDENT << "}\n\n";

        // Deferred (post-set) reaction setter: receives only the entity, so a single non-generic
        // Action<Entity> overload covers every property of this type.
        out << CS_INDENT << "public static void AddPropertyDeferredSetter(" << enum_type << " property, global::System.Action<" << entity_type << "> setter)\n";
        out << CS_INDENT << "{\n";
        out << CS_INDENT << "    global::FOnline.Native.AddPropertyDeferredSetter(\"" << target_name << "\", \"" << type_name << "\", property.ToString(), setter);\n";
        out << CS_INDENT << "}\n\n";

        // AngelScript's single `AddPropertySetter` accepts BOTH the value-transforming form (entity + ref value)
        // and an entity-only post-set REACTION callback (`void(Entity)`, registered via AddPostSetter, identical to
        // AddPropertyDeferredSetter). Expose the entity-only form as an AddPropertySetter overload too so a ported
        // `Game.AddPropertySetter(prop, void(Entity))` binds (the 1-arg Action vs the 2-arg PropertySetter<> value
        // overloads below resolve unambiguously by the callback's arity); it routes to the same post-set native.
        out << CS_INDENT << "public static void AddPropertySetter(" << enum_type << " property, global::System.Action<" << entity_type << "> setter)\n";
        out << CS_INDENT << "{\n";
        out << CS_INDENT << "    global::FOnline.Native.AddPropertyDeferredSetter(\"" << target_name << "\", \"" << type_name << "\", property.ToString(), setter);\n";
        out << CS_INDENT << "}\n\n";

        unordered_set<string> seen_value_types;
        vector<string> value_types;

        for (size_t i = 1; i < registrator->GetPropertiesCount(); i++) {
            ptr<const Property> prop = registrator->GetPropertyByIndexUnsafe(i);

            if (prop->IsDict() || prop->IsArray()) {
                continue;
            }

            const BaseTypeDesc& base_type = prop->GetBaseType();

            if (base_type.IsRefType || base_type.IsEntity) {
                continue;
            }

            string value_type = MakeCsTypeName(prop->GetBaseType());

            if (seen_value_types.emplace(value_type).second) {
                value_types.emplace_back(std::move(value_type));
            }
        }

        std::ranges::sort(value_types);

        for (const string& value_type : value_types) {
            out << CS_INDENT << "public static void AddPropertySetter(" << enum_type << " property, global::FOnline.PropertySetter<" << entity_type << ", " << value_type << "> setter)\n";
            out << CS_INDENT << "{\n";
            out << CS_INDENT << "    global::FOnline.Native.AddPropertySetter(\"" << target_name << "\", \"" << type_name << "\", property.ToString(), setter);\n";
            out << CS_INDENT << "}\n\n";

            out << CS_INDENT << "public static void AddPropertySetter(" << enum_type << " property, global::FOnline.PropertySetterWithProperty<" << entity_type << ", " << enum_type << ", " << value_type << "> setter)\n";
            out << CS_INDENT << "{\n";
            out << CS_INDENT << "    global::FOnline.Native.AddPropertySetterWithProperty(\"" << target_name << "\", \"" << type_name << "\", property.ToString(), setter);\n";
            out << CS_INDENT << "}\n\n";
        }
    }

    out << "    }\n\n";
}

static void AppendPropertyInfoAccessor(std::ostringstream& out, string_view type_name, const PropertyRegistrator* registrator, const map<int32_t, string>& enum_values)
{
    FO_STACK_TRACE_ENTRY();

    FO_VERIFY_AND_THROW(registrator, "Property registrator is null");

    const string enum_type = EscapeCsIdentifier(strex("{}Property", type_name).str());

    out << CS_INDENT << "public static void GetPropertyInfo(" << enum_type << " property, out bool isDisabled, out bool isVirtual, out bool isDict, out bool isArray, out bool isStringLike, out string enumName, out bool isInt, out bool isFloat, out bool isBool, out int baseSize, out bool isSynced)\n";
    out << CS_INDENT << "{\n";
    out << CS_INDENT << "    if (global::System.Convert.ToInt32(property) == 0)\n";
    out << CS_INDENT << "    {\n";
    out << CS_INDENT << "        throw new ArgumentOutOfRangeException(nameof(property), \"None is not valid property entry in this context\");\n";
    out << CS_INDENT << "    }\n\n";
    out << CS_INDENT << "    switch (property)\n";
    out << CS_INDENT << "    {\n";

    for (size_t i = 1; i < registrator->GetPropertiesCount(); i++) {
        ptr<const Property> prop = registrator->GetPropertyByIndexUnsafe(i);
        const auto enum_value = enum_values.find(numeric_cast<int32_t>(prop->GetRegIndex()));

        if (enum_value == enum_values.end()) {
            continue;
        }

        out << CS_INDENT << "    case " << enum_type << "." << EscapeCsIdentifier(enum_value->second) << ":\n";
        out << CS_INDENT << "        isDisabled = " << (prop->IsDisabled() ? "true" : "false") << ";\n";
        out << CS_INDENT << "        isVirtual = " << (prop->IsVirtual() ? "true" : "false") << ";\n";
        out << CS_INDENT << "        isDict = " << (prop->IsDict() ? "true" : "false") << ";\n";
        out << CS_INDENT << "        isArray = " << (prop->IsArray() ? "true" : "false") << ";\n";
        out << CS_INDENT << "        isStringLike = " << (prop->IsBaseTypeString() || prop->IsBaseTypeHash() ? "true" : "false") << ";\n";
        out << CS_INDENT << "        enumName = \"" << EscapeCsStringLiteral(prop->IsBaseTypeEnum() ? prop->GetBaseTypeName() : "") << "\";\n";
        out << CS_INDENT << "        isInt = " << (prop->IsBaseTypeInt() ? "true" : "false") << ";\n";
        out << CS_INDENT << "        isFloat = " << (prop->IsBaseTypeFloat() ? "true" : "false") << ";\n";
        out << CS_INDENT << "        isBool = " << (prop->IsBaseTypeBool() ? "true" : "false") << ";\n";
        out << CS_INDENT << "        baseSize = " << numeric_cast<int32_t>(prop->GetBaseSize()) << ";\n";
        out << CS_INDENT << "        isSynced = " << (prop->IsSynced() ? "true" : "false") << ";\n";
        out << CS_INDENT << "        return;\n";
    }

    out << CS_INDENT << "    default:\n";
    out << CS_INDENT << "        throw new ArgumentOutOfRangeException(nameof(property), property, \"Unknown property entry\");\n";
    out << CS_INDENT << "    }\n";
    out << CS_INDENT << "}\n\n";
}

static void AppendPropertyInfoAccessors(std::ostringstream& out, const EngineMetadata& meta)
{
    FO_STACK_TRACE_ENTRY();

    map<string, map<int32_t, string>> enum_values_by_index;

    for (const auto& [enum_name, enum_values] : MakeSortedEnums(meta)) {
        if (!enum_name.ends_with("Property")) {
            continue;
        }

        map<int32_t, string> values_by_index;

        for (const auto& [value_name, value] : enum_values) {
            values_by_index.emplace(value, value_name);
        }

        enum_values_by_index.emplace(enum_name, std::move(values_by_index));
    }

    out << "    public static partial class Game\n";
    out << "    {\n";

    for (const auto& [type_name, desc] : MakeSortedEntityTypes(meta.GetEntityTypes())) {
        const auto enum_it = enum_values_by_index.find(strex("{}Property", type_name).str());

        if (desc->PropRegistrator == nullptr || enum_it == enum_values_by_index.end()) {
            continue;
        }

        AppendPropertyInfoAccessor(out, type_name, desc->PropRegistrator.get(), enum_it->second);
    }

    for (const auto& [type_name, desc] : MakeSortedEntityTypes(meta.GetFixedTypes())) {
        const auto enum_it = enum_values_by_index.find(strex("{}Property", type_name).str());

        if (desc->PropRegistrator == nullptr || enum_it == enum_values_by_index.end()) {
            continue;
        }

        AppendPropertyInfoAccessor(out, type_name, desc->PropRegistrator.get(), enum_it->second);
    }

    out << "    }\n\n";
}

