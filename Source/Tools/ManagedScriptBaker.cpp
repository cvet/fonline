static auto MakeCsTypeToken(string_view name) -> string
{
    FO_STACK_TRACE_ENTRY();

    string result = EscapeCsIdentifier(name);

    if (!result.empty() && result.front() == '@') {
        result.erase(result.begin());
    }

    return result;
}

static auto MakeCsTypeName(const BaseTypeDesc& type) -> string
{
    FO_STACK_TRACE_ENTRY();

    if (type.IsBool) {
        return "bool";
    }
    if (type.IsInt8) {
        return "sbyte";
    }
    if (type.IsUInt8) {
        return "byte";
    }
    if (type.IsInt16) {
        return "short";
    }
    if (type.IsUInt16) {
        return "ushort";
    }
    if (type.IsInt32) {
        return "int";
    }
    if (type.IsUInt32) {
        return "uint";
    }
    if (type.IsInt64) {
        return "long";
    }
    if (type.IsUInt64) {
        return "ulong";
    }
    if (type.IsSingleFloat) {
        return "float";
    }
    if (type.IsDoubleFloat) {
        return "double";
    }
    if (type.IsString) {
        return "string";
    }
    if (type.IsHashedString) {
        return "hstring";
    }
    if (type.IsGlobalEntity && type.Name == "Game") {
        return "Game";
    }

    return EscapeCsIdentifier(type.Name);
}

static auto MakeCallbackDelegateName(const ComplexTypeDesc& type) -> string
{
    FO_STACK_TRACE_ENTRY();

    FO_VERIFY_AND_THROW(type.Kind == ComplexTypeKind::Callback, "Type kind must be a callback to build a delegate name");
    FO_VERIFY_AND_THROW(type.CallbackArgs, "Callback type has no argument list");

    string result = "Callback";

    for (const ComplexTypeDesc& callback_arg : *type.CallbackArgs) {
        result += "_";
        result += MakeCsTypeToken(callback_arg);
    }

    return result;
}

static auto MakeCsTypeName(const ComplexTypeDesc& type) -> string
{
    FO_STACK_TRACE_ENTRY();

    if (!type) {
        return "void";
    }

    switch (type.Kind) {
    case ComplexTypeKind::Simple:
        return MakeCsTypeName(type.BaseType);
    case ComplexTypeKind::Array:
        return strex("List<{}>", MakeCsTypeName(type.BaseType)).str();
    case ComplexTypeKind::Dict:
        FO_VERIFY_AND_THROW(type.KeyType, "Dictionary type has no key type");
        return strex("Dictionary<{}, {}>", MakeCsTypeName(type.KeyType.value()), MakeCsTypeName(type.BaseType)).str();
    case ComplexTypeKind::DictOfArray:
        FO_VERIFY_AND_THROW(type.KeyType, "Dictionary-of-array type has no key type");
        return strex("Dictionary<{}, List<{}>>", MakeCsTypeName(type.KeyType.value()), MakeCsTypeName(type.BaseType)).str();
    case ComplexTypeKind::Callback:
        return MakeCallbackDelegateName(type);
    default:
        break;
    }

    throw ManagedScriptBakerException("Unsupported C# complex type kind", numeric_cast<int32_t>(static_cast<std::underlying_type_t<ComplexTypeKind>>(type.Kind)));
}

static auto MakeCsTypeName(const ComplexTypeDesc& type, bool nullable) -> string
{
    FO_STACK_TRACE_ENTRY();

    string name = MakeCsTypeName(type);

    // Nullability is tracked only on handle/reference types (entities, strings, ref-types), which map
    // to C# reference types where `?` is valid; value types never carry the nullable bit.
    if (nullable) {
        name += "?";
    }

    return name;
}

static auto MakeCsTypeToken(const ComplexTypeDesc& type) -> string
{
    FO_STACK_TRACE_ENTRY();

    if (!type) {
        return "void";
    }

    switch (type.Kind) {
    case ComplexTypeKind::Simple:
        return MakeCsTypeToken(MakeCsTypeName(type.BaseType));
    case ComplexTypeKind::Array:
        return strex("{}Array", MakeCsTypeToken(type.BaseType.Name)).str();
    case ComplexTypeKind::Dict:
        FO_VERIFY_AND_THROW(type.KeyType, "Dictionary type has no key type");
        return strex("{}To{}", MakeCsTypeToken(type.KeyType->Name), MakeCsTypeToken(type.BaseType.Name)).str();
    case ComplexTypeKind::DictOfArray:
        FO_VERIFY_AND_THROW(type.KeyType, "Dictionary-of-array type has no key type");
        return strex("{}To{}Array", MakeCsTypeToken(type.KeyType->Name), MakeCsTypeToken(type.BaseType.Name)).str();
    case ComplexTypeKind::Callback:
        return MakeCallbackDelegateName(type);
    default:
        break;
    }

    throw ManagedScriptBakerException("Unsupported C# complex type kind", numeric_cast<int32_t>(static_cast<std::underlying_type_t<ComplexTypeKind>>(type.Kind)));
}

static auto MakeCsPropertyTypeName(const Property* prop) -> string
{
    FO_STACK_TRACE_ENTRY();

    FO_VERIFY_AND_THROW(prop, "Property is null");

    if (prop->IsDict()) {
        if (prop->IsDictOfArray()) {
            return strex("Dictionary<{}, List<{}>>", MakeCsTypeName(prop->GetDictKeyType()), MakeCsTypeName(prop->GetBaseType())).str();
        }

        return strex("Dictionary<{}, {}>", MakeCsTypeName(prop->GetDictKeyType()), MakeCsTypeName(prop->GetBaseType())).str();
    }
    if (prop->IsArray()) {
        return strex("List<{}>", MakeCsTypeName(prop->GetBaseType())).str();
    }

    return MakeCsTypeName(prop->GetBaseType());
}

static auto JoinCsCommaList(const vector<string>& values) -> string
{
    FO_STACK_TRACE_ENTRY();

    string result;

    for (const string& value : values) {
        if (!result.empty()) {
            result += ", ";
        }

        result += value;
    }

    return result;
}

static auto ShouldUseMultilineCsArgumentList(const vector<string>& values, size_t single_line_prefix_length) noexcept -> bool
{
    FO_NO_STACK_TRACE_ENTRY();

    if (values.size() > 4) {
        return true;
    }

    size_t line_length = single_line_prefix_length + 2;

    for (size_t i = 0; i < values.size(); i++) {
        line_length += values[i].length();

        if (i + 1 != values.size()) {
            line_length += 2;
        }
    }

    return line_length > 100;
}

static void AppendCsCallableDeclaration(std::ostringstream& out, string_view indent, string_view declaration_prefix, string_view name, const vector<string>& arg_declarations, string_view suffix)
{
    FO_STACK_TRACE_ENTRY();

    const string line_prefix = strex("{}{}{}", indent, declaration_prefix, name).str();

    if (ShouldUseMultilineCsArgumentList(arg_declarations, line_prefix.length() + suffix.length())) {
        out << line_prefix << "(\n";

        for (size_t i = 0; i < arg_declarations.size(); i++) {
            out << indent << "    " << arg_declarations[i] << (i + 1 != arg_declarations.size() ? "," : "") << "\n";
        }

        out << indent << ")" << suffix << "\n";
    }
    else {
        out << line_prefix << "(" << JoinCsCommaList(arg_declarations) << ")" << suffix << "\n";
    }
}

static auto MakeCsArgumentNames(const_span<ArgDesc> args) -> vector<string>
{
    FO_STACK_TRACE_ENTRY();

    vector<string> result;
    unordered_set<string> used_names;
    result.reserve(args.size());

    for (size_t i = 0; i < args.size(); i++) {
        const ArgDesc& arg = args[i];
        result.emplace_back(MakeUniqueCsIdentifier(arg.Name.empty() ? strex("arg{}", i).str() : arg.Name, used_names));
    }

    return result;
}

static auto MakeCsDefaultValueSuffix(const ArgDesc& arg) -> string
{
    FO_NO_STACK_TRACE_ENTRY();

    // C# method-parameter defaults must be compile-time constants. The runtime ArgDesc carries the normalized
    // C++/AngelScript default (ArgDesc.DefaultValue, e.g. "0" / "false" / "null" / "hstring()" / "Enum::Member"),
    // so translate it -- without this a ported call that omits a defaulted argument (e.g. Game.GetText(key) over
    // GetText(key, skipCount = 0)) fails to compile. ref/out params and params without a default get no suffix.
    // The universal fallback is "= default" (valid for any C# type): exact for empty value-struct ctors / the
    // empty hstring, and a zeroed best-effort for the rare ctor-with-arguments default.
    if (arg.DefaultValue.empty() || arg.Type.IsMutable) {
        return "";
    }

    const string& default_value = arg.DefaultValue;
    const char first_ch = default_value.front();

    // Numeric / bool / string / char literals are valid C# constants verbatim.
    if (first_ch == '"' || first_ch == '\'' || first_ch == '-' || first_ch == '+' || first_ch == '.' || (first_ch >= '0' && first_ch <= '9') || default_value == "true" || default_value == "false") {
        return " = " + default_value;
    }

    // Enum member reference (Enum::Member) -> Enum.Member; enum members are C# compile-time constants.
    if (default_value.find("::") != string::npos && default_value.find('(') == string::npos) {
        return " = " + strex(default_value).replace("::", ".").str();
    }

    // nullptr or a value-struct ctor (hstring()/ipos()/...): not expressible as a C# constant, so use `= default`.
    return " = default";
}

static auto MakeCsArgumentDeclarations(const_span<ArgDesc> args) -> vector<string>
{
    FO_STACK_TRACE_ENTRY();

    vector<string> result;
    const vector<string> arg_names = MakeCsArgumentNames(args);
    result.reserve(args.size());

    for (size_t i = 0; i < args.size(); i++) {
        const ArgDesc& arg = args[i];
        string declaration;

        if (arg.Type.IsMutable) {
            declaration += "ref ";
        }

        declaration += MakeCsTypeName(arg.Type, arg.Nullable);
        declaration += " ";
        declaration += arg_names[i];
        declaration += MakeCsDefaultValueSuffix(arg);
        result.emplace_back(std::move(declaration));
    }

    return result;
}

static auto MakeCsEventArgumentDeclarations(string_view owner_type_name, bool is_global, const_span<ArgDesc> args) -> vector<string>
{
    FO_STACK_TRACE_ENTRY();

    vector<string> result;
    unordered_set<string> used_names;
    result.reserve(args.size() + (!is_global ? 1u : 0u));

    if (!is_global) {
        const string self_name = MakeUniqueCsIdentifier(strex(owner_type_name).lower().str(), used_names);
        result.emplace_back(strex("{} {}", EscapeCsIdentifier(owner_type_name), self_name).str());
    }

    for (size_t i = 0; i < args.size(); i++) {
        const ArgDesc& arg = args[i];
        const string arg_name = MakeUniqueCsIdentifier(arg.Name.empty() ? strex("arg{}", i).str() : arg.Name, used_names);
        string declaration;

        if (arg.Type.IsMutable) {
            declaration += "ref ";
        }

        declaration += MakeCsTypeName(arg.Type, arg.Nullable);
        declaration += " ";
        declaration += arg_name;
        result.emplace_back(std::move(declaration));
    }

    return result;
}

