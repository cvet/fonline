static auto HashManagedSolutionGuid(string_view value, uint64_t seed) noexcept -> uint64_t
{
    FO_NO_STACK_TRACE_ENTRY();

    constexpr uint64_t FNV_OFFSET = 14695981039346656037ull;
    constexpr uint64_t FNV_PRIME = 1099511628211ull;

    uint64_t hash = FNV_OFFSET ^ seed;

    for (const char c : value) {
        hash ^= static_cast<uint8_t>(c);
        hash *= FNV_PRIME;
    }

    return hash;
}

static auto MakeManagedSolutionGuid(string_view value) -> string
{
    FO_STACK_TRACE_ENTRY();

    const uint64_t first = HashManagedSolutionGuid(value, 0x6D616E6167656401ull);
    const uint64_t second = HashManagedSolutionGuid(value, 0x736F6C7574696F6Eull);

    std::ostringstream output;
    output << '{' << std::uppercase << std::hex << std::setfill('0');
    output << std::setw(8) << numeric_cast<uint32_t>((first >> 32) & 0xFFFFFFFFull);
    output << '-';
    output << std::setw(4) << numeric_cast<uint16_t>((first >> 16) & 0xFFFFull);
    output << '-';
    output << std::setw(4) << numeric_cast<uint16_t>(first & 0xFFFFull);
    output << '-';
    output << std::setw(4) << numeric_cast<uint16_t>(second >> 48);
    output << '-';
    output << std::setw(12) << (second & 0x0000FFFFFFFFFFFFull);
    output << '}';

    const std::string result = output.str();
    return string {result.c_str()};
}

