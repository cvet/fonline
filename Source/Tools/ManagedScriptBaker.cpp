static void AppendPropertyGroupGetters(std::ostringstream& out, const EngineMetadata& meta)
{
    FO_STACK_TRACE_ENTRY();

    // AngelScript registers, per entity type, a `<Type>PropertyGroup` namespace with a getter per property group
    // returning `array<<Type>Property>` of the group's properties (AngelScriptGlobals.cpp's register loop). The
    // managed side emits the same surface as a `static class <Type>PropertyGroup` with a static property per group.
    // Group membership is known at bake time, so each list is built from the property reg-indices (which ARE the
    // `<Type>Property` enum values) -- no native bridge or enum-member-name matching needed.
    unordered_set<string> enum_names;

    for (const auto& enum_entry : MakeSortedEnums(meta)) {
        enum_names.emplace(enum_entry.first);
    }

    for (const auto& [type_name, desc] : MakeSortedEntityTypes(meta.GetEntityTypes())) {
        if (desc->IsGlobal) {
            continue;
        }

        const string property_enum_name = strex("{}Property", type_name).str();

        if (!enum_names.contains(property_enum_name)) {
            continue;
        }

        nptr<const PropertyRegistrator> registrator = meta.GetPropertyRegistrator(type_name);

        if (!registrator) {
            continue;
        }

        const map<string, vector<ptr<const Property>>> groups = registrator->GetPropertyGroups();

        if (groups.empty()) {
            continue;
        }

        const string enum_type = EscapeCsIdentifier(property_enum_name);

        out << "    public static partial class " << EscapeCsIdentifier(strex("{}PropertyGroup", type_name).str()) << "\n";
        out << "    {\n";

        bool first = true;

        for (const auto& [group_name, properties] : groups) {
            if (!first) {
                out << "\n";
            }

            first = false;

            out << CS_INDENT << "public static global::System.Collections.Generic.List<" << enum_type << "> " << EscapeCsIdentifier(group_name) << "\n";
            out << CS_INDENT << "{\n";
            out << CS_INDENT << "    get\n";
            out << CS_INDENT << "    {\n";
            out << CS_INDENT << "        return new global::System.Collections.Generic.List<" << enum_type << ">\n";
            out << CS_INDENT << "        {\n";

            for (ptr<const Property> prop : properties) {
                out << CS_INDENT << "            (" << enum_type << ")(" << prop->GetRegIndex() << "),\n";
            }

            out << CS_INDENT << "        };\n";
            out << CS_INDENT << "    }\n";
            out << CS_INDENT << "}\n";
        }

        out << "    }\n\n";
    }
}

static void AppendCustomEntityProtoGetters(std::ostringstream& out, const EngineMetadata& meta)
{
    FO_STACK_TRACE_ENTRY();

    // The built-in (Exported) entities (Item/Critter/Location/Map) already have metadata-exported Game.GetProto*
    // accessors. Custom entity protos and fixed types register their getters only at AngelScript runtime
    // (register_entity_protos / register_fixed_type in AngelScriptEntity.cpp), so the managed side emits the same
    // surface here, backed by the Native.GetProtoEntity/CheckProtoEntity bridges:
    //  - custom HasProtos entities (Modifier/Faction): Game.GetProto<X>(pid) -> Proto<X>, CheckProto<X>(pid);
    //  - fixed types (QuestData/WeatherType/ItemBag/...): Game.Get<X>(pid) -> <X>, Check<X>(pid) (the fixed type is
    //    its own proto, so the method has no "Proto" prefix and returns the type directly).
    struct ProtoGetter
    {
        string MethodName;
        string CheckName;
        string ReturnType;
        string NativeType;
    };

    vector<ProtoGetter> getters;

    for (const auto& [type_name, desc] : MakeSortedEntityTypes(meta.GetEntityTypes())) {
        if (type_name != "Game" && desc->HasProtos && !desc->Exported) {
            const string proto_class = strex("Proto{}", type_name).str();
            getters.push_back({strex("GetProto{}", type_name).str(), strex("CheckProto{}", type_name).str(), proto_class, type_name});
        }
    }

    for (const auto& [type_name, desc] : MakeSortedEntityTypes(meta.GetFixedTypes())) {
        ignore_unused(desc);
        getters.push_back({strex("Get{}", type_name).str(), strex("Check{}", type_name).str(), type_name, type_name});
    }

    if (getters.empty()) {
        return;
    }

    out << "    // Proto getters for custom entity protos + fixed types (mirrors AngelScript register_entity_protos /\n";
    out << "    // register_fixed_type); the built-in entities expose Game.GetProto* via metadata instead.\n";
    out << "    public static partial class Game\n";
    out << "    {\n";

    bool first = true;

    for (const ProtoGetter& getter : getters) {
        if (!first) {
            out << "\n";
        }

        first = false;

        out << CS_INDENT << "public static " << getter.ReturnType << " " << getter.MethodName << "(hstring pid)\n";
        out << CS_INDENT << "{\n";
        out << CS_INDENT << "    IntPtr __ptr = global::FOnline.Native.GetProtoEntity(\"" << getter.NativeType << "\", pid.Value);\n";
        out << CS_INDENT << "    Verify(__ptr != IntPtr.Zero, \"Proto entity not found (check the Check-accessor first)\");\n";
        out << CS_INDENT << "    return global::FOnline.Native.WrapEntity<" << getter.ReturnType << ">(__ptr);\n";
        out << CS_INDENT << "}\n\n";

        out << CS_INDENT << "public static bool " << getter.CheckName << "(hstring pid)\n";
        out << CS_INDENT << "{\n";
        out << CS_INDENT << "    return global::FOnline.Native.CheckProtoEntity(\"" << getter.NativeType << "\", pid.Value);\n";
        out << CS_INDENT << "}\n\n";

        out << CS_INDENT << "public static System.Collections.Generic.List<" << getter.ReturnType << "> " << getter.MethodName << "s()\n";
        out << CS_INDENT << "{\n";
        out << CS_INDENT << "    int __count = global::FOnline.Native.GetProtoEntityCount(\"" << getter.NativeType << "\");\n";
        out << CS_INDENT << "    System.Collections.Generic.List<" << getter.ReturnType << "> __result = new System.Collections.Generic.List<" << getter.ReturnType << ">(__count);\n";
        out << CS_INDENT << "    for (int __i = 0; __i < __count; __i++)\n";
        out << CS_INDENT << "    {\n";
        out << CS_INDENT << "        __result.Add(global::FOnline.Native.WrapEntity<" << getter.ReturnType << ">(global::FOnline.Native.GetProtoEntityAt(\"" << getter.NativeType << "\", __i)));\n";
        out << CS_INDENT << "    }\n";
        out << CS_INDENT << "    return __result;\n";
        out << CS_INDENT << "}\n";
    }

    out << "    }\n\n";
}

static auto MakeEnumUnderlyingCsType(const BaseTypeDesc& enum_type) -> string
{
    FO_STACK_TRACE_ENTRY();

    const BaseTypeDesc* underlying_type = enum_type.EnumUnderlyingType.get();

    if (underlying_type == nullptr) {
        return "int";
    }

    return MakeCsTypeName(*underlying_type);
}

static void CollectCallbacks(const ComplexTypeDesc& type, unordered_map<string, ComplexTypeDesc>& callbacks)
{
    FO_STACK_TRACE_ENTRY();

    if (!type) {
        return;
    }

    if (type.Kind == ComplexTypeKind::Callback) {
        callbacks.emplace(MakeCallbackDelegateName(type), type);

        for (const ComplexTypeDesc& callback_arg : *type.CallbackArgs) {
            CollectCallbacks(callback_arg, callbacks);
        }
    }
}

static void CollectCallbacks(const vector<ArgDesc>& args, unordered_map<string, ComplexTypeDesc>& callbacks)
{
    FO_STACK_TRACE_ENTRY();

    for (const ArgDesc& arg : args) {
        CollectCallbacks(arg.Type, callbacks);
    }
}

static void CollectCallbacks(const EngineMetadata& meta, unordered_map<string, ComplexTypeDesc>& callbacks)
{
    FO_STACK_TRACE_ENTRY();

    for (const auto& [type_name, desc] : meta.GetEntityTypes()) {
        for (const MethodDesc& method : desc.Methods) {
            CollectCallbacks(method.Ret, callbacks);
            CollectCallbacks(method.Args, callbacks);
        }
        for (const EntityEventDesc& event : desc.Events) {
            CollectCallbacks(event.Args, callbacks);
        }

        ignore_unused(type_name);
    }

    for (const auto& [type_name, desc] : meta.GetFixedTypes()) {
        for (const MethodDesc& method : desc.Methods) {
            CollectCallbacks(method.Ret, callbacks);
            CollectCallbacks(method.Args, callbacks);
        }

        ignore_unused(type_name);
    }

    for (const auto& [type_name, type] : meta.GetBaseTypes()) {
        if (type.IsRefType && type.RefType) {
            for (const MethodDesc& method : type.RefType->Methods) {
                CollectCallbacks(method.Ret, callbacks);
                CollectCallbacks(method.Args, callbacks);
            }
        }

        ignore_unused(type_name);
    }
}

static auto MakeSortedBaseTypes(const EngineMetadata& meta) -> vector<const BaseTypeDesc*>
{
    FO_STACK_TRACE_ENTRY();

    vector<const BaseTypeDesc*> result;
    result.reserve(meta.GetBaseTypes().size());

    for (const auto& type : meta.GetBaseTypes() | std::views::values) {
        result.emplace_back(&type);
    }

    std::ranges::sort(result, {}, [](const BaseTypeDesc* type) { return type->Name; });
    return result;
}

static auto MakeSortedEnums(const EngineMetadata& meta) -> vector<pair<string, map<string, int32_t>>>
{
    FO_STACK_TRACE_ENTRY();

    vector<pair<string, map<string, int32_t>>> result;
    result.reserve(meta.GetAllEnums().size());

    for (const auto& [enum_name, enum_values] : meta.GetAllEnums()) {
        map<string, int32_t> sorted_values;

        for (const auto& [value_name, value] : enum_values) {
            sorted_values.emplace(value_name, value);
        }

        result.emplace_back(enum_name, std::move(sorted_values));
    }

    std::ranges::sort(result, {}, [](const auto& entry) -> const string& { return entry.first; });
    return result;
}

static auto MakeSortedEntityTypes(const map<hstring, EntityTypeDesc>& types) -> vector<pair<string, const EntityTypeDesc*>>
{
    FO_STACK_TRACE_ENTRY();

    vector<pair<string, const EntityTypeDesc*>> result;
    result.reserve(types.size());

    for (const auto& [type_name, desc] : types) {
        result.emplace_back(type_name.as_str(), &desc);
    }

    std::ranges::sort(result, {}, [](const auto& entry) -> const string& { return entry.first; });
    return result;
}

static void WriteTextFileIfChanged(const std::filesystem::path& file_path, string_view content, string_view error_message)
{
    FO_STACK_TRACE_ENTRY();

    const std::string new_content {content.data(), content.size()};

    if (std::ifstream existing_file(file_path, std::ios::binary); existing_file) {
        std::ostringstream existing_content;
        existing_content << existing_file.rdbuf();

        if (existing_content.str() == new_content) {
            return;
        }
    }

    std::error_code ec;
    (void)std::filesystem::create_directories(file_path.parent_path(), ec);

    if (ec) {
        throw ManagedScriptBakerException(error_message, file_path.string());
    }

    std::filesystem::path temp_path = file_path;
    temp_path += ".tmp";

    ec.clear();
    (void)std::filesystem::remove(temp_path, ec);

    {
        std::ofstream file(temp_path, std::ios::binary | std::ios::trunc);

        if (!file) {
            throw ManagedScriptBakerException(error_message, file_path.string());
        }

        file.write(new_content.data(), numeric_cast<std::streamsize>(new_content.size()));
    }

    ec.clear();
    (void)std::filesystem::remove(file_path, ec);

    if (ec) {
        std::error_code remove_temp_ec;
        (void)std::filesystem::remove(temp_path, remove_temp_ec);
        throw ManagedScriptBakerException(error_message, file_path.string());
    }

    ec.clear();
    std::filesystem::rename(temp_path, file_path, ec);

    if (ec) {
        std::error_code remove_temp_ec;
        (void)std::filesystem::remove(temp_path, remove_temp_ec);
        throw ManagedScriptBakerException(error_message, file_path.string());
    }
}

static void WriteGeneratedFile(const std::filesystem::path& project_dir, string_view target_name, string_view suffix, string_view content)
{
    FO_STACK_TRACE_ENTRY();

    const string file_name = MakeGeneratedManagedApiFileName(target_name, suffix);
    const std::filesystem::path file_path = project_dir / file_name;
    WriteTextFileIfChanged(file_path, content, "Can't create generated C# file");

    std::error_code ec;
    std::filesystem::remove(project_dir / strex("{}{}.cs", target_name, suffix).str(), ec);
}

static auto ReadFileBytes(const std::filesystem::path& path) -> vector<uint8_t>
{
    FO_STACK_TRACE_ENTRY();

    const string path_str = strex("{}", path.string()).str();
    const optional<string> data = fs_read_file(path_str);

    if (!data.has_value()) {
        throw ManagedScriptBakerException("Can't read Managed file", path_str);
    }

    return vector<uint8_t>(data->begin(), data->end());
}

static auto CollectManagedOutputAssemblies(const std::filesystem::path& assemblies_output_dir, string_view target_name) -> vector<std::filesystem::path>
{
    FO_STACK_TRACE_ENTRY();

    const std::filesystem::path output_dir = assemblies_output_dir / strex("{}Assemblies", target_name).str();
    vector<std::filesystem::path> result;

    std::error_code ec;

    for (std::filesystem::directory_iterator it(output_dir, ec); !ec && it != std::filesystem::directory_iterator(); it.increment(ec)) {
        if (!it->is_regular_file()) {
            continue;
        }
        if (it->path().extension() != ".dll") {
            continue;
        }

        result.emplace_back(it->path());
    }

    if (ec) {
        throw ManagedScriptBakerException("Can't enumerate Managed output assemblies", output_dir.string());
    }

    std::ranges::sort(result, {}, [](const std::filesystem::path& path) { return path.filename().string(); });
    return result;
}

static void RemoveManagedOutputAssemblies(const std::filesystem::path& assemblies_output_dir, string_view target_name)
{
    FO_STACK_TRACE_ENTRY();

    const std::filesystem::path output_dir = assemblies_output_dir / strex("{}Assemblies", target_name).str();

    std::error_code ec;

    for (std::filesystem::directory_iterator it(output_dir, ec); !ec && it != std::filesystem::directory_iterator(); it.increment(ec)) {
        if (!it->is_regular_file()) {
            continue;
        }
        if (it->path().extension() != ".dll") {
            continue;
        }

        std::error_code remove_ec;
        std::filesystem::remove(it->path(), remove_ec);
    }

    if (ec) {
        std::error_code dir_ec;
        const bool output_dir_exists = std::filesystem::is_directory(output_dir, dir_ec);

        if (output_dir_exists) {
            throw ManagedScriptBakerException("Can't enumerate Managed output assemblies", output_dir.string());
        }

        std::error_code create_ec;
        std::filesystem::create_directories(output_dir, create_ec);
    }
}

static void RemoveManagedBuildSidecars(const std::filesystem::path& assemblies_output_dir, string_view target_name, string_view assembly_file_name)
{
    FO_STACK_TRACE_ENTRY();

    const std::filesystem::path output_dir = assemblies_output_dir / strex("{}Assemblies", target_name).str();
    string assembly_file_stem = strex(assembly_file_name).erase_file_extension().str();

    std::error_code ec;
    std::filesystem::remove(output_dir / strex("{}.deps.json", assembly_file_stem).str(), ec);
    ec.clear();
    std::filesystem::remove(output_dir / strex("{}.pdb", assembly_file_stem).str(), ec);
}

static void AppendProjectReferences(std::ostream& file, const std::filesystem::path& project_dir, const vector<string>& references, optional<string_view> condition)
{
    FO_STACK_TRACE_ENTRY();

    if (references.empty()) {
        return;
    }

    file << "  <ItemGroup";

    if (condition.has_value()) {
        file << " Condition=\"" << EscapeXml(*condition) << "\"";
    }

    file << ">\n";

    for (const string& reference : references) {
        const bool is_path_reference = reference.find('\\') != string::npos || reference.find('/') != string::npos || std::filesystem::path(reference).extension().string() == ".dll";

        if (is_path_reference) {
            const std::filesystem::path reference_path = reference;
            const std::filesystem::path hint_path = reference_path.is_absolute() ? reference_path : std::filesystem::current_path() / reference_path;
            file << "    <Reference Include=\"" << EscapeXml(reference_path.stem().string()) << "\">\n";
            file << "      <HintPath>" << EscapeXml(MakeRelativeProjectPath(project_dir, hint_path)) << "</HintPath>\n";
            file << "    </Reference>\n";
        }
        else {
            file << "    <Reference Include=\"" << EscapeXml(reference) << "\" />\n";
        }
    }

    file << "  </ItemGroup>\n";
}

