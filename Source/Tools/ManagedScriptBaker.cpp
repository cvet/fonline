auto ManagedScriptBaker::SplitEnvList(const char* raw) -> vector<string>
{
    FO_NO_STACK_TRACE_ENTRY();

    if (raw == nullptr || *raw == '\0') {
        return {};
    }

    vector<string> result;
    string token;

    const auto flush_token = [&]() {
        token = TrimString(std::move(token));

        if (!token.empty()) {
            result.emplace_back(std::move(token));
            token.clear();
        }
    };

    for (const char c : string_view(raw)) {
        if (c == ';' || c == ',' || c == '|') {
            flush_token();
            continue;
        }

        token.push_back(c);
    }

    flush_token();
    return result;
}

static auto GetManagedBakeStamp(const BakingContext& context, string_view target_name, const vector<std::filesystem::path>& source_files, const vector<string>& references) -> uint64_t
{
    FO_STACK_TRACE_ENTRY();

    uint64_t stamp = 1;

    const auto merge_disk_file = [&](const string& path) {
        if (const uint64_t write_time = fs_last_write_time(path); write_time != 0) {
            stamp = std::max(stamp, write_time);
        }
    };

    for (const std::filesystem::path& source_file : source_files) {
        const string source_file_path = strex("{}", source_file.string()).str();
        merge_disk_file(source_file_path);
    }

    for (const string& reference : references) {
        if (reference.find('/') != string::npos || reference.find('\\') != string::npos || reference.find(':') != string::npos || strex(reference).get_file_extension() == "dll") {
            merge_disk_file(reference);
        }
    }

    if (context.BakedFiles) {
        const string target_lower = strex(target_name).lower();
        const FileHeader metadata_header = context.BakedFiles->ReadFileHeader(strex("Metadata.fometa-{}", target_lower));

        if (metadata_header) {
            stamp = std::max(stamp, metadata_header.GetWriteTime());
        }
    }

    return stamp;
}

